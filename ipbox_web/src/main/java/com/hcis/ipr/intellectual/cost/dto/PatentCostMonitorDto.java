package com.hcis.ipr.intellectual.cost.dto;

import org.apache.ibatis.annotations.Param;

import java.math.BigDecimal;
import java.util.Date;

/**
 * @author zhw
 * @date 2019/9/23
 **/
public class PatentCostMonitorDto {

    private String id;

    private String email;

    private String patentName;

    private BigDecimal feePayable;

    private String userName;

    private String appNumber;

    private String costName;

    private Date standardDate;

    public String getPatentName() {
        return patentName;
    }

    public void setPatentName(String patentName) {
        this.patentName = patentName;
    }

    public String getId() {
        return id;
    }

    public void setId(String id) {
        this.id = id;
    }

    public String getEmail() {
        return email;
    }

    public void setEmail(String email) {
        this.email = email;
    }

    public BigDecimal getFeePayable() {
        return feePayable;
    }

    public void setFeePayable(BigDecimal feePayable) {
        this.feePayable = feePayable;
    }

    public String getUserName() {
        return userName;
    }

    public void setUserName(String userName) {
        this.userName = userName;
    }

    public String getAppNumber() {
        return appNumber;
    }

    public void setAppNumber(String appNumber) {
        this.appNumber = appNumber;
    }

    public String getCostName() {
        return costName;
    }

    public void setCostName(String costName) {
        this.costName = costName;
    }

    public Date getStandardDate() {
        return standardDate;
    }

    public void setStandardDate(Date standardDate) {
        this.standardDate = standardDate;
    }
}
