package com.hcis.ipr.train.register.model;

import com.hcis.ipanther.common.excel.model.ExcelModel;

public class CourseRegisterModel extends ExcelModel{

	private String courseRegisterId;//选课表Id
	private String studentName;//教师名称
	private String deptName;//学校名称
	private String courseName;//课程名称
	private String score;//分数
	private String result;
	
	public String getCourseRegisterId() {
		return courseRegisterId;
	}
	public void setCourseRegisterId(String courseRegisterId) {
		this.courseRegisterId = courseRegisterId;
	}
	public String getStudentName() {
		return studentName;
	}
	public void setStudentName(String studentName) {
		this.studentName = studentName;
	}
	public String getDeptName() {
		return deptName;
	}
	public void setDeptName(String deptName) {
		this.deptName = deptName;
	}
	public String getCourseName() {
		return courseName;
	}
	public void setCourseName(String courseName) {
		this.courseName = courseName;
	}
	public String getScore() {
		return score;
	}
	public void setScore(String score) {
		this.score = score;
	}
	public String getResult() {
		return result;
	}
	public void setResult(String result) {
		this.result = result;
	}

}
