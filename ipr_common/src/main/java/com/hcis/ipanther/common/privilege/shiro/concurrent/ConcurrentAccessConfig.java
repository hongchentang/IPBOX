package com.hcis.ipanther.common.privilege.shiro.concurrent;

public class ConcurrentAccessConfig
{
  private int maxConcurrentAccess = 1;
  private String accountIdKey;

  public int getMaxConcurrentAccess()
  {
    return this.maxConcurrentAccess;
  }
  public void setMaxConcurrentAccess(int maxConcurrentAccess) {
    this.maxConcurrentAccess = maxConcurrentAccess;
  }
  public String getAccountIdKey() {
    return this.accountIdKey;
  }
  public void setAccountIdKey(String accountIdKey) {
    this.accountIdKey = accountIdKey;
  }
}