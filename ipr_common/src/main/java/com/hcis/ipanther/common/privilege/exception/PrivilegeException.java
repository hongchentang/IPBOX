package com.hcis.ipanther.common.privilege.exception;

import com.hcis.ipanther.core.exception.IPantherException;

public class PrivilegeException extends IPantherException
{
  private static final long serialVersionUID = -1279504371447000527L;

  public PrivilegeException()
  {
  }

  public PrivilegeException(String msg)
  {
    super(msg);
  }

  public PrivilegeException(String msg, Throwable ex) {
    super(msg, ex);
  }

  public PrivilegeException(Throwable ex) {
    super(ex);
  }
}