package com.hcis.ipanther.common.customers.entity;

import com.hcis.ipanther.core.entity.BaseEntity;

public class CustomersEmail extends BaseEntity {

	private String userName;
	private String userEmail;
	private String companyName;
	private String mobilePhoe;
	private String userId = "N";
	private String status;
	private String toTime;

	public String getUserName() {
		return userName;
	}

	public void setUserName(String userName) {
		this.userName = userName;
	}

	public String getUserEmail() {
		return userEmail;
	}

	public void setUserEmail(String userEmail) {
		this.userEmail = userEmail;
	}

	public String getCompanyName() {
		return companyName;
	}

	public void setCompanyName(String companyName) {
		this.companyName = companyName;
	}

	public String getMobilePhoe() {
		return mobilePhoe;
	}

	public void setMobilePhoe(String mobilePhoe) {
		this.mobilePhoe = mobilePhoe;
	}

	public String getUserId() {
		return userId;
	}

	public void setUserId(String userId) {
		this.userId = userId;
	}

	public String getStatus() {
		return status;
	}

	public void setStatus(String status) {
		this.status = status;
	}

	public String getToTime() {
		return toTime;
	}

	public void setToTime(String toTime) {
		this.toTime = toTime;
	}

}
