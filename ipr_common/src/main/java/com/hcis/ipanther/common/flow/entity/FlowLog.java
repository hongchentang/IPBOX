package com.hcis.ipanther.common.flow.entity;

import java.util.Date;

import com.hcis.ipanther.core.entity.BaseEntity;
/**
 * 通用流程日志实体类
 * @author wuwentao
 * @date 2015年3月30日
 */
public class FlowLog extends BaseEntity{
	
	private static final long serialVersionUID = -8483136379921828933L;
	
	//日志表名
	private String tableName;
	//业务ID的字段名
	private String businessColumnName;
	//业务ID
	private String businessId;
	//任务名称
    private String taskName;
    //任务ID
    private String taskId;
    //审核部门
    private String auditDept;
    //审核时间
    private Date auditTime;
    //审核人
    private String auditUser;
    //审核结果 字典：FLOW_AUDIT_RESULT  FlowConstants.AUDIT_RESULT
    private String auditResult;
    //审核内容、备注信息
    private String auditContent;
    //状态 字典：FLOW_STATUS FlowConstants.FLOW_STATUS_*
    private String status;
    
    /**
     * 无数据库对应字段
     */
    private String auditUserName;//审核人姓名
    private String auditDeptName;//审核部门姓名
    
	public String getTableName() {
		return tableName;
	}
	public void setTableName(String tableName) {
		this.tableName = tableName;
	}
	public String getBusinessColumnName() {
		return businessColumnName;
	}
	public void setBusinessColumnName(String businessColumnName) {
		this.businessColumnName = businessColumnName;
	}
	public String getBusinessId() {
		return businessId;
	}
	public void setBusinessId(String businessId) {
		this.businessId = businessId;
	}
	public String getTaskName() {
		return taskName;
	}
	public void setTaskName(String taskName) {
		this.taskName = taskName;
	}
	public String getTaskId() {
		return taskId;
	}
	public void setTaskId(String taskId) {
		this.taskId = taskId;
	}
	public String getAuditDept() {
		return auditDept;
	}
	public void setAuditDept(String auditDept) {
		this.auditDept = auditDept;
	}
	public Date getAuditTime() {
		return auditTime;
	}
	public void setAuditTime(Date auditTime) {
		this.auditTime = auditTime;
	}
	public String getAuditUser() {
		return auditUser;
	}
	public void setAuditUser(String auditUser) {
		this.auditUser = auditUser;
	}
	public String getAuditResult() {
		return auditResult;
	}
	public void setAuditResult(String auditResult) {
		this.auditResult = auditResult;
	}
	public String getAuditContent() {
		return auditContent;
	}
	public void setAuditContent(String auditContent) {
		this.auditContent = auditContent;
	}
	public String getStatus() {
		return status;
	}
	public void setStatus(String status) {
		this.status = status;
	}
	public String getAuditUserName() {
		return auditUserName;
	}
	public void setAuditUserName(String auditUserName) {
		this.auditUserName = auditUserName;
	}
	public String getAuditDeptName() {
		return auditDeptName;
	}
	public void setAuditDeptName(String auditDeptName) {
		this.auditDeptName = auditDeptName;
	}

}