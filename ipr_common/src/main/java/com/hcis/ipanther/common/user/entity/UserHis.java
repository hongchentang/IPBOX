package com.hcis.ipanther.common.user.entity;

/**
 * 用户历史表 
 * 保存用户的基本信息
 * @author wuwentao
 * @date 2015年4月9日
 */
public class UserHis extends User{
	
	private static final long serialVersionUID = 7086455105301237311L;
	//主键
	private String id;
	//关联用户的ID
	private String userId;
	//用户所属的部门
	private String deptId;

	public String getId() {
		return id;
	}

	public void setId(String id) {
		this.id = id;
	}

	public String getUserId() {
		return userId;
	}

	public void setUserId(String userId) {
		this.userId = userId;
	}

	public String getDeptId() {
		return deptId;
	}

	public void setDeptId(String deptId) {
		this.deptId = deptId;
	}

}